var game = {
    time: 0,
    last: 0,
    nextUser: 0,
    users: [],
};

wss.on("connection", ws => {
    ws.on("message", message => {
        try {
            var data = JSON.parse(message);
        } catch {
            return console.log;
        }

        switch (data.type) {
            case "CONNECT":
                console.log("CONNECT", data);

                var id = game.nextUser;

                game.users.push({
                    id,
                    x: 0,
                    y: 0,
                    velocity: {x: 0, y: 0},
                    input: {
                        mouse: {x: 0, y: 0, click: false},
                        keys: {}
                    },
                    halfwidth: data.halfwidth,
                    halfheight: data.halfheight
                });

                send(ws, {
                    type: "READY",
                    user_id: id,
                    users: game.users,
                    map
                });

                ws.user_id = id;

                game.nextUser++;

                break;
            case "PLAYER_UPDATE":
                // console.log("PLAYER_UPDATE", data);

                if (ws.user_id == undefined) return;

                var index = game.users.findIndex(user => user.id == ws.user_id);

                game.users[index].input = data.input;

                break;
            default:
                console.log("UNKNOWN", data);
        }
    });

    ws.on("close", () => {
        if (ws.user_id !== undefined) {
            var index = game.users.findIndex(user => user.id == ws.user_id)

            game.users.splice(index, 1);
        }
    });
});

function send(ws, message) {
    ws.send(JSON.stringify(message));
}

function GameObject(options) {
    for (option in options) {
        this[option] = options[option];
    }

    if (!this.init) this.init = function() {};
    if (!this.update) this.update = function() {};
    if (!this.draw) this.draw = function() {};

    this.init();

    game.world.push(this);
}

